from dataclasses import dataclass
from enum import Enum


class Approach(str, Enum):
    """Рекомендуемый архитектурный подход для системы на основе языковой модели."""

    RAG = "rag"
    FINE_TUNING = "fine_tuning"
    HYBRID = "hybrid"


@dataclass
class Requirements:
    """Параметры требований к системе для архитектурного анализа."""

    data_update_frequency_days: int
    """Как часто обновляются данные (в днях). 1 = ежедневно, 30 = ежемесячно."""

    requires_source_citation: bool
    """Должна ли система указывать источник (документ/раздел) для каждого ответа."""

    domain_style_critical: bool
    """Критичен ли специфический стиль или строгий формат вывода (юридический язык, JSON и т.п.)."""

    data_volume_docs: int
    """Примерный объём базы знаний (количество документов)."""

    budget_rubles: int
    """Бюджет на разработку в рублях."""


@dataclass
class Recommendation:
    """Результат архитектурного анализа."""

    approach: Approach
    """Рекомендуемый подход."""

    reason: str
    """Обоснование выбора на русском языке."""

    estimated_dev_weeks: int
    """Примерное время разработки прототипа в неделях."""


